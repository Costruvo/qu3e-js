//--------------------------------------------------------------------------------------------------
// q3Math.js
// JavaScript port of Randy Gaul's q3Math
//--------------------------------------------------------------------------------------------------

// Equivalent to FLT_MAX
const Q3_R32_MAX = Number.MAX_VALUE;

//--------------------------------------------------------------------------------------------------

const q3PI = 3.14159265;

//--------------------------------------------------------------------------------------------------
// Scalar Math
//--------------------------------------------------------------------------------------------------

function q3Invert(a)
{
    return a !== 0.0 ? 1.0 / a : 0.0;
}

//--------------------------------------------------------------------------------------------------

function q3Sign(a)
{
    if (a >= 0.0)
        return 1.0;
    else
        return -1.0;
}

//--------------------------------------------------------------------------------------------------

function q3Abs(a)
{
    return a < 0.0 ? -a : a;
}

//--------------------------------------------------------------------------------------------------

function q3Min(a, b)
{
    return a < b ? a : b;
}

//--------------------------------------------------------------------------------------------------

function q3Max(a, b)
{
    return a > b ? a : b;
}

//--------------------------------------------------------------------------------------------------

function q3Clamp(val, min, max)
{
    if (isNaN(val)) return min; // safety fallback
    if (val < min) return min;
    if (val > max) return max;
    return val;
}

function q3Clamp01(val)
{
    if (isNaN(val)) return 0;
    if (val <= 0.0) return 0;
    if (val >= 1.0) return 1;
    return val;
}
//--------------------------------------------------------------------------------------------------

function q3Lerp(a, b, t)
{
    return a * (1.0 - t) + b * t;
}

//--------------------------------------------------------------------------------------------------
// Vec3 Lerp
//--------------------------------------------------------------------------------------------------

function q3LerpVec3(a, b, t)
{
    return new q3Vec3(
        a.x * (1.0 - t) + b.x * t,
        a.y * (1.0 - t) + b.y * t,
        a.z * (1.0 - t) + b.z * t
    );
}

//--------------------------------------------------------------------------------------------------
// Random
//--------------------------------------------------------------------------------------------------

function q3RandomFloat(l, h)
{
    let a = Math.random();
    a = (h - l) * a + l;
    return a;
}

//--------------------------------------------------------------------------------------------------

function q3RandomInt(low, high)
{
    return Math.floor(Math.random() * (high - low + 1)) + low;
}